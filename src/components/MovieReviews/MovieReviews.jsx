import { useOutletContext } from "react-router-dom";

export default function MovieReviews() {
  const { reviews } = useOutletContext();

  return (
    <div>
      <h2>Reviews</h2>
      <ul>
        {reviews.map(review => (
          <li key={review.id}>
            <h3>{review.author}</h3>
            <p>{review.content}</p>
          </li>
        ))}
      </ul>
    </div>
  );
}
